function loadHerbs() {
  return JSON.parse(fs.readFileSync(MAPPING, 'utf8'));
}

function main() {
  const rows = loadHerbs();
  const summary = JSON.parse(fs.readFileSync(SUMMARY, 'utf8'));
  const ORDER = summary.body_category_order;

  // body_category → tag → herb_category(어부/초부/…) → [herb]
  const tree = {};
  for (const r of rows) {
    if (!r.tags || r.tags.length === 0) continue;
    const herbObj = {
      id: r.herb_id,
      korean_name: r.herb_korean || '',
      hanja_name: r.herb_hanja || '',
    };
    for (const tag of r.tags) {
      const bcat = BODY_CATEGORY_OF_TAG[tag] || '기타';
      const subCat = r.category || '기타';
      tree[bcat] = tree[bcat] || {};
      tree[bcat][tag] = tree[bcat][tag] || {};
      tree[bcat][tag][subCat] = tree[bcat][tag][subCat] || [];
      tree[bcat][tag][subCat].push(herbObj);
    }
  }

  // 정렬·평탄화
  const categories = [];
  const ordered = ORDER.filter((c) => tree[c]).concat(
    Object.keys(tree).filter((c) => !ORDER.includes(c))
  );
  for (const bcat of ordered) {
    const efficacyMap = tree[bcat];
    const efficacies = Object.keys(efficacyMap)
      .sort((a, b) => a.localeCompare(b, 'ko'))
      .map((tag) => {
        const groups = Object.keys(efficacyMap[tag])
          .sort((a, b) => a.localeCompare(b, 'ko'))
          .map((sub) => ({
            category: sub,
            label: sub,
            herbs: efficacyMap[tag][sub]
              .slice()
              .sort((a, b) => (a.korean_name || '').localeCompare(b.korean_name || '', 'ko')),
          }));
        return { tag, groups };
      });
    categories.push({ key: bcat, efficacies });
  }

  const out = { categories, generated_at: new Date().toISOString() };
  fs.writeFileSync(OUT_JSON, JSON.stringify(out, null, 2), 'utf8');

  // efficacy-universe-2d.js / 3d.js가 바로 읽을 수 있게 window 전역으로 노출하는 JS 빌드도 함께 출력
  const jsContent =
    '// AUTO-GENERATED by scripts/build-efficacy-universe.js\n' +
    '// 이 파일은 직접 수정하지 말 것 — 빌드 스크립트를 다시 실행할 것\n' +
    '(function () {\n' +
    "  'use strict';\n" +
    '  if (typeof window === "undefined") return;\n' +
    '  window.EFFICACY_UNIVERSE_DATA = ' + JSON.stringify(out) + ';\n' +
    '})();\n';
  fs.writeFileSync(OUT_JS, jsContent, 'utf8');

  // 요약 출력
  let totalEffNodes = 0;
  let totalGroupNodes = 0;
  let totalHerbLinks = 0;
  for (const cat of categories) {
    totalEffNodes += cat.efficacies.length;
    for (const eff of cat.efficacies) {
      totalGroupNodes += eff.groups.length;
      for (const grp of eff.groups) totalHerbLinks += grp.herbs.length;
    }
  }
  console.log('=== efficacy-universe 빌드 결과 ===');
  console.log(`신체 부위 카테고리: ${categories.length}`);
  console.log(`효능 태그(중복 포함): ${totalEffNodes}`);
  console.log(`서브그룹(약재 분류): ${totalGroupNodes}`);
  console.log(`약재 ↔ 효능 링크: ${totalHerbLinks}`);
  console.log('\n--- 카테고리별 효능 수 ---');
  for (const cat of categories) {
    console.log(`  ${cat.key.padEnd(8, ' ')}\t효능 ${String(cat.efficacies.length).padStart(2)}\t약재링크 ${
      cat.efficacies.reduce((s, e) => s + e.groups.reduce((ss, g) => ss + g.herbs.length, 0), 0)
    }`);
  }
  console.log(`\n출력: ${path.relative(ROOT, OUT_JSON)}, ${path.relative(ROOT, OUT_JS)}`);
}

main();
